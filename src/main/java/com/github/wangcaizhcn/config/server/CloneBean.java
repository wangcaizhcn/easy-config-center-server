package com.github.wangcaizhcn.config.server;

public class CloneBean {

	private String version;
	
	private String cloneVersion;
	
	private String tag;
	
	private String cloneTag;

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getCloneVersion() {
		return cloneVersion;
	}

	public void setCloneVersion(String cloneVersion) {
		this.cloneVersion = cloneVersion;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

	public String getCloneTag() {
		return cloneTag;
	}

	public void setCloneTag(String cloneTag) {
		this.cloneTag = cloneTag;
	}
	
}
